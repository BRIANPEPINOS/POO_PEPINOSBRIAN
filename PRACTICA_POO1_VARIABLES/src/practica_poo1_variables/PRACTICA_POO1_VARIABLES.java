public class PRACTICA_POO1_VARIABLES {

 
    public static void main(String[] args) {
        byte nume=5;
        short num1=6;
        int num2=3;
        float num3=3.45f;
        double num4=4.5;
        char c='a';
        String nombre="brian";
        boolean i=5>15;
        System.out.println("byte: "+nume);
        System.out.println("short: "+num1);
        System.out.println("int: "+num2);
        System.out.println("float: "+num3);
        System.out.println("double: "+num4);
        System.out.println("char: "+c);
        System.out.println("string: "+nombre);
        System.out.println("boolean: "+i);
        
    }
    
}
