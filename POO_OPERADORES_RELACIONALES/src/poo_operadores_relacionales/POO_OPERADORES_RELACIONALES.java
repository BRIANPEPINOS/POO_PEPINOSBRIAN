//operadores relacionales
package poo_operadores_relacionales;

public class POO_OPERADORES_RELACIONALES {

    public static void main(String[] args) {
        int num1=1;
        int num2=1;
        System.out.println("igual: "+(num1==num2));
        System.out.println("negacion: "+(num1!= num2));
        System.out.println("mayor que: "+(num1>num2));
        System.out.println("mayor o igual: "+(num1>=num2));
        System.out.println("menor que: "+(num1<num2));
        System.out.println("menor igual que: "+(num1<=num2));
       
    }
    
}
